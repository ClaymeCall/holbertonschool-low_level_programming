#include "3-calc.h"

/**
 *op_add - Function that adds two integers.
 *@a : Integer 1
 *@b : Integer 2
 *
 *Return: The sum, as int.
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 *op_sub - Function that substracts two integers.
 *@a : Integer 1
 *@b : Integer 2
 *
 *Return: The substraction, as int.
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 *op_mul - Function that multiplies two integers.
 *@a : Integer 1
 *@b : Integer 2
 *
 *Return: The product, as int.
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 *op_div - Function that divides two integers.
 *@a : Integer 1
 *@b : Integer 2
 *
 *Return: The quotient division, as int.
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 *op_mod - Function that divides two integers.
 *@a : Integer 1
 *@b : Integer 2
 *
 *Return: The remainder of the division, as int.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
